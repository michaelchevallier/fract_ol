#include "fractol.h"

void		color_initialiser(t_env **e)
{
	(*e)->r = (*e)->i * 255 / (*e)->max_iter;
	(*e)->g = (*e)->i * 255 / (*e)->max_iter;
	(*e)->b = (*e)->i * 255 / (*e)->max_iter;
}
